_BY_CODE = {code: (code, name, sector) for code, name, sector in MASTER}


def to_symbol(code):
    """銘柄コード（例 '7203'）を Yahoo シンボル（'7203.T'）に変換。
    すでに .T 付きや指数(^N225)・為替(USDJPY=X)ならそのまま返す。"""
    code = str(code).strip().upper()
    if code.endswith(".T") or code.startswith("^") or "=" in code:
        return code
    return code + ".T"


def to_code(symbol):
    """Yahoo シンボルから素のコードを取り出す（'7203.T' -> '7203'）。"""
    symbol = str(symbol).strip().upper()
    if symbol.endswith(".T"):
        return symbol[:-2]
    return symbol


def name_of(code):
    """マスターに載っていれば銘柄名を返す。無ければ None。"""
    rec = _BY_CODE.get(str(code).strip().upper())
    return rec[1] if rec else None


def sector_of(code):
    rec = _BY_CODE.get(str(code).strip().upper())
    return rec[2] if rec else None


def search(query):
    """コードまたは名称の部分一致で検索。"""
    q = (query or "").strip().lower()
    if not q:
        return []
    out = []
    for code, name, sector in MASTER:
        if q in code.lower() or q in name.lower() or q in sector.lower():
            out.append({"code": code, "name": name, "sector": sector})
    return out
